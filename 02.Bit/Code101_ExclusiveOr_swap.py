#   不借助第三个变量交换两个数 （主要针对非python语言）
#############################################################################

def swap(a,b):
    a = a^b
    b = a^b
    a = a^b
    return a, b

def main():
    a = 10
    b = 20
    a, b = swap(a,b)
    print(a,b)


if __name__ == "__main__":
    main()   